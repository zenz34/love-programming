/**
 * // Definition for a Node.
 * function Node(val,children) {
 *    this.val = val;
 *    this.children = children;
 * };
 */
/**
 * @param {Node} root
 * @return {number[][]}
 */
var levelOrder = function(root) {
    let queue = [];
    let ans = [];
    
    if (root !== null) {
        queue.push(root);
    }
  
    while (queue.length > 0) {
      let len = queue.length;
      let tmp = [];
      
      for (let i = 0; i < len; i++) {
          const node = queue.shift();
          tmp.push(node.val);
  
        for (let child of node.children) {
            queue.push(child);
        }
      }
  
      ans.push(tmp);
    }
  
    return ans;
};

